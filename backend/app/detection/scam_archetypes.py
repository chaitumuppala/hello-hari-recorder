def _match_keywords(text_lower: str, words: set[str], keywords: set[str]) -> str | None:
    """Return first matching keyword found in text, or None."""
    for kw in keywords:
        kw_lower = kw.lower()
        if kw_lower in text_lower:
            return kw
        # Stemmed matching for English prefixes like "deactivat", "expir"
        # Require both word and keyword to be 4+ chars to avoid false
        # positives from short words like "to", "i", "for", "the".
        for word in words:
            if len(word) >= 4 and len(kw_lower) >= 4:
                if word.startswith(kw_lower) or kw_lower.startswith(word):
                    return kw
    return None


def check_keyword_cooccurrence(text: str, detected: list[str]) -> int:
    """Check all scam archetypes for keyword co-occurrence.

    Returns the highest score across all archetypes.
    """
    lower = text.lower()
    words = set(lower.split())
    collapsed = lower.replace(" ", "")  # For spaced transliterations

    best_score = 0

    for label, ctx_kws, thr_kws, dem_kws in SCAM_ARCHETYPES:
        ctx_hit = _match_keywords(lower, words, ctx_kws)
        thr_hit = _match_keywords(lower, words, thr_kws)
        dem_hit = _match_keywords(lower, words, dem_kws)

        # Collapsed-space fallback for demand (e.g. "ओ टी पी" -> "ओटीपी")
        if not dem_hit:
            dem_hit = _match_keywords(collapsed, set(collapsed.split()), dem_kws)

        has_ctx = ctx_hit is not None
        has_thr = thr_hit is not None
        has_dem = dem_hit is not None

        score = 0
        if has_ctx and has_thr and has_dem:
            score = 95
            detected.append(
                f"[KEYWORD_COOCCUR:{label}] context({ctx_hit}) + "
                f"threat({thr_hit}) + demand({dem_hit}) (+95)"
            )
        elif has_thr and has_dem:
            score = 80
            detected.append(
                f"[KEYWORD_COOCCUR:{label}] threat({thr_hit}) + "
                f"demand({dem_hit}) (+80)"
            )
        elif has_ctx and has_dem:
            score = 75
            detected.append(
                f"[KEYWORD_COOCCUR:{label}] context({ctx_hit}) + "
                f"demand({dem_hit}) (+75)"
            )
        elif has_ctx and has_thr:
            score = 70
            detected.append(
                f"[KEYWORD_COOCCUR:{label}] context({ctx_hit}) + "
                f"threat({thr_hit}) (+70)"
            )

        if score > best_score:
            best_score = score

    return best_score
